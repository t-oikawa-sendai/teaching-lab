試験結果_判定 = ("A", "B", "C", "D")

# 2. ユーザーからの入力を受け取って辞書に格納
試験_点数 = {
    "英語": int(input("英語は？ ")),
    "数学": int(input("数学は？ ")),
    "国語": int(input("国語は？ "))
}

print("-" * 20)

# 3. 各科目の判定処理
for 科目, 点数 in 試験_点数.items():
    # 判定ロジック
    if 80 <= 点数 <= 100:
        結果 = 試験結果_判定[0]  # A
    elif 70 <= 点数 < 80:
        結果 = 試験結果_判定[1]  # B
    elif 60 <= 点数 < 70:
        結果 = 試験結果_判定[2]  # C
    else:
        結果 = 試験結果_判定[3]  # D

    print(f"{科目}: {点数}点 -> 判定: {結果}")